# Data Type #
## List
x = ["one", "Two", "Three"]
print(type(x))
print(x)
print(x[0])


## Tuple

q = ("one", "two", "three")
print(type(q))
print(q)
print(q[0])


## Dict

y = {"name" : "Mohamed", "age" : "31"}
print(type(y))
print(y)
print(y.items())
print(y.keys())
print(y.values())
print(y['name'])
