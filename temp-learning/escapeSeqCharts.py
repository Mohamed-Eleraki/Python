# Backspace
# \b
print("hello \bworld") # will remove the space

# Tab
# \t
print("hello\tworld") # will add a tab

# new line
# \n
print("hello\nworld") # will add a new line 

# escape new line
# \
print("I \
love \
Python")  # will print I love Python on the same line

# backslash escape backslash
# \\
print("path//to/")  # will print path//to/

# Escape quotes
# \"
print('I love "Python"')  # will print I love "Python"
print('I love \"Python\"')  # will print I love "Python"

# Carriage return
# \r
print("hello\rworld") # will remove the hello and print world, by replacing the numbers of world with the numbers of hello

# character hex value
# \x
print("\x48\x65\x6c\x6c\x6f")  # will print Hello, check: https://www.freecodecamp.org/news/ascii-table-hex-to-ascii-value-character-code-chart-2/

